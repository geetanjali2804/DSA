//traversing in array
package BASICS;
class _01program{
    public static void main(String[]args){
        int [] arr = {10,20,30,40,50};
        //traversing each element of an array
        for(int i =0;i < arr.length;i++){
            System.out.println("Element at index " + i + " is " + arr[i]);
        }
    }
}